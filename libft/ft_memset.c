#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*d;
	size_t	i;

	i = 0;
	d = (char *)b;
	while (i < len)
	{
		*d = c;
		d++;
		i++;
	}
	return (b);
}
